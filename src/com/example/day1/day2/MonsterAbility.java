package com.example.day1.day2;

public class MonsterAbility {
    private int damage = 5;
    private int hp = 100;

    public MonsterAbility(){}

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }
}
